	function hashPassword($password) 
	{
		$hash = password_hash($password, PASSWORD_DEFAULT);
		return $hash;
	}
	
	function resultBlock($errors){
		if(count($errors) > 0)
		{
			echo "<div id='error' class='alert alert-danger' role='alert'>
			<a href='#' onclick=\"showHide('error');\">[X]</a>
			<ul>";
			foreach($errors as $error)
			{
				echo "<li>".$error."</li>";
			}
			echo "</ul>";
			echo "</div>";
		}
	}
	
	function registraUsuario($usuario, $pass_hash, $nombre, $email, $activo, $tipo_usuario, $dni, $telefono){
		
		global $mysqli;

		$stmt = $mysqli->prepare("INSERT INTO turista (usuario, password, nombre, correo, activacion, id_tipo, dni, telefono) VALUES(?,?,?,?,?,?,?,?)");
		$stmt->bind_param('ssssiiii', $usuario, $pass_hash, $nombre, $email, $activo, $tipo_usuario, $dni, $telefono);
		
		if ($stmt->execute()){
			return $mysqli->insert_id;
			} else {
			return 0;	
		}		
	}
	
